def ascii_tabular(string) : 
    """
    This is where you input a string,
    and return the ASCII code of it.
    """
    s = string 
    return ord(s)

def ascii_tabular(integer) : 
    """
    This one is the reverse,
    you input the ASCII code and get the charachter in return 
    """
    i = integer
    return chr(i)